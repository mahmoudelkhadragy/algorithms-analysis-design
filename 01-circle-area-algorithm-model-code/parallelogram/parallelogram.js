// calculate area of parallelogram

/*
  input: b & h
    b => base of parallelogram
    h => height of parallelogram

  process: area = b * h

  output: area
*/

function parallelogramArea(b, h) {
  const area = b * h;
  return area;
}

console.log(parallelogramArea(5, 3)); // 15
console.log(parallelogramArea(4, 6)); // 24
